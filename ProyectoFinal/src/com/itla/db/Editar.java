    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtName.getText();
        String apellido = txtApellido.getText();
        String correo = txtCorreo.getText();
        String telefono = txtTelefono.getText();
        String empresa = txtEmpresa.getText();
        if (nombre.isEmpty() || apellido.isEmpty() || correo.isEmpty() || telefono.isEmpty() || empresa.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, completa todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            contactUpdated = new Contacto(nombre, apellido, empresa, correo, telefono);
            DBManager.editarContacto(contactUpdated, contact.getId());
            JOptionPane.showMessageDialog(null, "Transaccion realizada", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            Vista vista = new Vista();
            vista.setVisible(true);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

